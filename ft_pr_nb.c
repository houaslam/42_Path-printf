#include "ft_printf.h"

void	ft_pr_nb(int nb, int *i)
{
	if (nb == -2147483648)
	{
		*i += write (1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			ft_pr_char('-', i);
			nb = nb * -1;
		}
		if (nb >= 10)
		{
			ft_pr_nb(nb / 10, i);
		}
		ft_pr_char(nb % 10 + '0', i);
	}
}
